    public void getUsersToPanel(){
    
        try{
            
            stmt = con.createStatement();
            con = Database_connection_CLASS.connection();
            String query = "SELECT * FROM notes ";
            ResultSet rsd = stmt.executeQuery(query);

            System.out.println(userid);
            while(rsd.next())
            {
                System.out.println(userid);
                if(userid.equals(rsd.getString(2))){
                    System.out.println(userid);
                    String subject = rsd.getString(3);
                    String note =rsd.getString(4);
                    String ref = rsd.getString(5);
                    
                    System.out.println(subject + " "+note+" "+ref);
                    
                    NoteDetails item = new NoteDetails (subject,note,ref);
                    jPanel1.add(item, "wrap, w 80%");
                    jPanel1.repaint();
                    jPanel1.revalidate();
                    
                }      
            }
        }
        catch(SQLException e){
        
        }
    }
    
    public void screateCode(){
     
        try{
            
            stmt = con.createStatement();
            String query = "SELECT * FROM users ";
            ResultSet rsd = stmt.executeQuery(query);

            while(rsd.next())
            {
                if (userid.equals(rsd.getString(2)))
                {
                    String secret_code = rsd.getString(10);
                    String salt_value = (rsd.getString(11));
                    PasswordDetails.SECRET_KEY = secret_code; PasswordDetails.SALTVALUE = salt_value;
  
                }
            }
        
        }
        catch(SQLException e){
            
        }
    }

